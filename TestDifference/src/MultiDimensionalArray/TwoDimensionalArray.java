public class TwoDimensionalArray {
    public static void main(String[] args) 
  {
      int rows=6,cols=5;
      int i,j;
      for(i=0;i<rows;i++)
      {
          for(j=0;j<cols;j++)
          {
              System.out.println();
          }
      }
      
  }
}
